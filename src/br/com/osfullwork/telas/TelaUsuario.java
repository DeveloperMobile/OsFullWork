    private void consultar() {

        String sql = "select * from tbusuarios where iduser=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuId.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                txtUsuNome.setText(rs.getString(2));
                txtUsuFone.setText(rs.getString(3));
                txtUsuLogin.setText(rs.getString(4));
                txtUsuSenha.setText(rs.getString(5));
                cboUsuPerfil.setSelectedItem(rs.getString(6));
            } else {
                JOptionPane.showMessageDialog(null, "Usuário não cadastrado!");
                // As linhas abaixo limpam os campos
                txtUsuNome.setText(null);
                txtUsuFone.setText(null);
                txtUsuLogin.setText(null);
                txtUsuSenha.setText(null);
                //cboUsuPerfil.setSelectedItem(null);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
            //System.out.println(e.getMessage());
        }
    }

    private void adicionar() {

        String sql = "insert into tbusuarios(iduser, usuario, fone, login, senha, perfil)"
                + " values(?,?,?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuId.getText());
            pst.setString(2, txtUsuNome.getText());
            pst.setString(3, txtUsuFone.getText());
            pst.setString(4, txtUsuLogin.getText());
            pst.setString(5, txtUsuSenha.getText());
            pst.setString(6, cboUsuPerfil.getSelectedItem().toString());

            // Validação dos campos obrigatórios
            if (((txtUsuId.getText().isEmpty()) || (txtUsuNome.getText().isEmpty())) 
                    || (txtUsuLogin.getText().isEmpty() || (txtUsuSenha.getText().isEmpty()))){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");
            } else {
                // A linha abaixo atualiza a tabela de usuários com os dados dos campos dos formulário
                // A estrutura abaixo é usada para confirmar a inserção dos dados na tabela
                int adicionado = pst.executeUpdate();
                // A linha abaixo serve de apoio ao entendimento da lógica
                // System.out.println(adicionado);
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Usuário adicionado com sucesso!");
                    txtUsuId.setText(null);
                    txtUsuNome.setText(null);
                    txtUsuFone.setText(null);
                    txtUsuLogin.setText(null);
                    txtUsuSenha.setText(null);
                    //cboUsuPerfil.setSelectedItem(null);
                }
            }
        } catch (SQLException e) {               
            if (e.getMessage().equals("Duplicate entry '" + txtUsuId.getText() + "' for key 'PRIMARY'")) {
                JOptionPane.showMessageDialog(null, "O usuário com id " + txtUsuId.getText() + " já está cadastrado!");
            } else {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }            
            //System.out.println(e.getMessage());             
        }
    }
    // método para alterar dados do usuário
    private void alterar() {
        
        String sql = "update tbusuarios set usuario=?,fone=?,login=?,senha=?,perfil=? where iduser=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuNome.getText());
            pst.setString(2, txtUsuFone.getText());
            pst.setString(3, txtUsuLogin.getText());
            pst.setString(4, txtUsuSenha.getText());
            pst.setString(5, cboUsuPerfil.getSelectedItem().toString());
            pst.setString(6, txtUsuId.getText());
            
            // Validação dos campos obrigatórios
            if (((txtUsuId.getText().isEmpty()) || (txtUsuNome.getText().isEmpty())) 
                    || (txtUsuLogin.getText().isEmpty() || (txtUsuSenha.getText().isEmpty()))){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");
            } else {
                // A linha abaixo atualiza a tabela de usuários com os dados dos campos dos formulário
                // A estrutura abaixo é usada para confirmar a alteração dos dados na tabela
                int adicionado = pst.executeUpdate();
                // A linha abaixo serve de apoio ao entendimento da lógica
                // System.out.println(adicionado);
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Dados do usuário alterados com sucesso!");
                    txtUsuId.setText(null);
                    txtUsuNome.setText(null);
                    txtUsuFone.setText(null);
                    txtUsuLogin.setText(null);
                    txtUsuSenha.setText(null);
                    //cboUsuPerfil.setSelectedItem(null);
                }
            }
        } catch (SQLException e) {
            switch (e.getMessage()) {
                case "Data truncation: Data too long for column 'fone' at row 1":
                    JOptionPane.showMessageDialog(null, "Dados muito longos para a coluna 'Fone'! Use no máximo até 15 caracteres!");
                    break;
                case "Data truncation: Data too long for column 'login' at row 1":
                    JOptionPane.showMessageDialog(null, "Dados muito longos para a coluna 'Login'! Use no máximo até 15 caracteres!");
                    break;
                case "Data truncation: Data too long for column 'usuario' at row 1":
                    JOptionPane.showMessageDialog(null, "Dados muito longos para a coluna 'Nome'! Use no máximo até 50 caracteres!");
                    break;
                case "Data truncation: Data too long for column 'senha' at row 1":
                    JOptionPane.showMessageDialog(null, "Dados muito longos para a coluna 'Senha'! Use no máximo até 15 caracteres!");
                    break;
                default:
                    JOptionPane.showMessageDialog(null, e);
                    break;
            }
            //System.out.println(e.getMessage());
        }
    }
    
    // método responsável pela remoção de usuários
    private void remover() {
        
        // a estrutura abaixo confirma a remoção do usuário
        int confirma = JOptionPane.showConfirmDialog(null,"Tem certeza que deseja remover este usuário?","Atenção!",JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            String sql = "delete from tbusuarios where iduser=?";
            try {
                pst = conexao.prepareStatement(sql);
                pst.setString(1,txtUsuId.getText());
                int apagado = pst.executeUpdate();
                if (apagado > 0) {
                    JOptionPane.showMessageDialog(null, "Usuário removido com sucesso!");
                    txtUsuId.setText(null);
                    txtUsuNome.setText(null);
                    txtUsuFone.setText(null);
                    txtUsuLogin.setText(null);
                    txtUsuSenha.setText(null);
                    //cboUsuPerfil.setSelectedItem(null);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
                //System.out.println(e.getMessage());
            }
        }
    }
    
